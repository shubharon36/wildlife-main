/**
 * Resolves featured sanctuary cards for the selected habitat (by name) within a region.
 * Use when the `habitat` reference might not include nested data.
 */
export function getFeaturedSanctuariesForHabitat(region, habitat) {
  if (!region?.habitats || !habitat?.name) return []
  const match = region.habitats.find((h) => h.name === habitat.name)
  return match?.featuredSanctuaries ?? []
}
